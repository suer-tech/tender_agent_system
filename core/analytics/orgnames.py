def _compile_long(forms: list[str]) -> re.Pattern:
    """Скомпилировать regex с пробелами как `\\s+` — чтобы ловить двойные
    пробелы в исходных данных ЕИС."""
    expanded = [re.sub(r" +", r"\\s+", f) for f in forms]
    return re.compile(r"^(?:" + "|".join(expanded) + r")\b\s*", re.IGNORECASE)


